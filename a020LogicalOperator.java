//Logical Operator
//  && (logical and)
//  ||  (logical or)
//   !  (logical not)

// this will return true or false
public class a020LogicalOperator {
    public static void main(String[] args) {
        System.out.println((3>4)&&(4>5));
        System.out.println((3>2)|| (5>0));
        System.out.println(!(5>2));
    }
}
