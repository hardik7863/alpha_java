//Assignment operator
//  =
//  +=
//  -=
//  *=
//  /=
public class a021assignmentoperator {
    public static void main(String[] args) {
        int a=10;
        a+=10;// this will be more faster in execution in  nano seconds
        System.out.println(a);
        int b=5;
        b%=5;
        System.out.println(b);
        
    }
}
