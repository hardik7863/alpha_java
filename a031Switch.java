public class a031Switch {
    public static void main(String[] args) {
        int number =2;
        switch (number) {
            case 1:
                System.out.println("samosa");
                break;
            case 2:
                System.out.println("burger");
                break;
            case 3:
                System.out.println("mango shake");
                break;
            default: System.out.println("ur dreaming");
                break;
        }
    }
}
